public class DataTypes {

	public static void main(String[] args) {
		byte number = -128;
		System.out.println(number);

		char sampleUnicode = '\u05E2';
		System.out.println(sampleUnicode);

		float sampleNum1 = 1.123456289564456f;
		System.out.println(sampleNum1);

		float sampleNum2 = 10012.123456289564456f;
		System.out.println(sampleNum2);
		
		double sampleNum3 = 10012.123456289564456;
		System.out.println(sampleNum3);
	}
}
