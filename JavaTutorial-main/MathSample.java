public class MathSample {
    public static void main(String[] args) {
        System.out.println(Math.min(23, 11)); // To find min number of 2 arguments
        System.out.println(Math.max(23, 11)); // To find max number of 2 arguments
        System.out.println(Math.abs(-10.25)); // To absolute and provide positive integer
        System.out.println(Math.random()); // To generate random double integer
        System.out.println(Math.sqrt(5)); // To find squre root of a given number
        System.out.println(Math.pow(2, 2)); // To find power of a given number
        System.out.println(Math.pow(3, 4));
    }
}

