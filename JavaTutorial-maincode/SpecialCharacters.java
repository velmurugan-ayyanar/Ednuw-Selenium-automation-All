public class SpecialCharacters {
    public static void main(String[] args) {
        char singleQuote = '\'';
        System.out.println(singleQuote);

        String doubleQuotes = "My company name is \"Ednue\"";
        System.out.println(doubleQuotes);

        String slash = "This is backslash -> \\";
        System.out.println(slash);

        // To print multiple lines
        String multilineText = "This is 1st Line\nThis\\n is 2st Line\nThis is 3st Line";
        System.out.println(multilineText);
    }
}
