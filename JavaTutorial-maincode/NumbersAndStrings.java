public class NumbersAndStrings {
    public static void main(String[] args) {
        int num1 = 100;
        int num2 = 200;
        System.out.println(num1 + num2); // Performs addition

        String num3 = "100";
        int num4 = 200;
        System.out.println(num3 + num4); // Performs concatenation

        int num5 = 100;
        String num6 = "200";
        System.out.println(num5 + num6); // Performs concatenation
    }
}
